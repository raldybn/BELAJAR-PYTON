#list
def maka(x,y):
    return [-18*x/y,1/2*x+y]
jika_1=10
jika_2=4

hasil=maka(jika_1,jika_2)
print(f"hasil dari-18.x/y adalah {hasil[0]}, lalu hasil dari 1/2*x+y adalah {hasil[1]}")

